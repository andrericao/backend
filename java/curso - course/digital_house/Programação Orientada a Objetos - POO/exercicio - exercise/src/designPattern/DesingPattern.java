    /*

    -Um padrão de projeto, também é muito conhecido pelo termo original do inglês “Design Pattern”, no qual descreve
	 uma solução geral reutilizável para um problema recorrente no desenvolvimento de sistemas . Não se trata de um código pronto,
	 uma versão final, mas sim um modelo de como resolver um determinado problema. Os padrões de projeto definem as relações
	 e as interações entre classes ou objetos, sem especificar os detalhes dos envolvidos. Em termos conceituais, um padrão de projeto
	 deve definir: um nome, o problema, a solução, quando aplicar essa solução e as consequências dessa solução.

	 Não se trata de um framework ou um código pronto, mas de uma definição de alto nível de como um problema comum pode
	 ser solucionado. Conhecer Design Patterns é algo de extrema importância no desenvolvimento de qualquer software.
	 A utilização desses padrões nos ajuda a desenvolver de forma mais rápida frente a desafios semelhantes.


    - Vantagens:
        - Agilizar o desenvolvimento, são SUGESTÕES DE USO.
        - Sistema com menos bugs
        - Permite mais tempo pra focar mas próximas etapas e agilidade na entrega do projeto.


	São 3 tipos:
	-CREATIONAL ou criacional: objetivo é abstrair como os objetos são criados. Fornece interfaces ou classes abstratas para criar e
		copiar objetos existentes e criar famílias de objetos relacionados sem 	especificar suas classes concretas.
		Permite construir objetos passo a passo e produzir diferentes tipos e representações usando o mesmo código
		de construção. São os padrões:
		                    Abstract Factory
							Factory Method
							Builder
							Prototype
							Singleton


	-STRUCTURAL ou estrutural: lidam com a composição de uma classe e objetos. Permite a colaboração de objetos
		de interfaces incompativeis, adicionando novos comportamentos e colocando eles dentro deum invultorio de
		objetos que contem comportamentos, permite que vc faça divisão de uma classe ou um conjunto de classes
		intimamente ligadas em duas hierarquias separadas ABSTRAÇÃO e IMPLEMENTAÇÃO que podem ser desenvolvidas
		indepedente umas das outras. Permite Composição em estrutura de árvores e então trabalhar com essa
		estrutura como se fossem objetos individuais. São os padrões:
		                    Adapter
							Bridge
							Composite
							Decorator
							Façade
							Flywight
							Proxy
	-BEHAVIOURAL ou comportamentais: relações entre objetos e classes e suas distribuições de responsabilidades.
			São os padrões: Interpreter
							Template Method
							Chain of Responsabilit
							Iterator
							Command
							Mediator
							Memento
							Observer
							State
							Strategy
							Visitor

 */

